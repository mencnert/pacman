	// methods for loadmap()
	public void addPoint(int x, int y) {
		Point p = new Point(x, y);
		points.add(p);
	}

	public void addBlock(int x, int y) {
		Block b = new Block(x, y);
		blocks.add(b);
	}

	public void addGhost(int x, int y) {
		Ghost gh = new Ghost(x, y);
		ghosts.add(gh);
	}

	public void pacmanPointCollision() {
		for (int i = 0; i < points.size(); i++) {
			Point p = (Point) points.get(i);
			if (pacman.getCenterX() == p.getX() && pacman.getCenterY() == p.getY()) {
				points.remove(i);
				score += 1;
				coin.play();
			}
		}

		if (score > bestScore) {
			bestScore = score;
			bestPlayer = null;
		}
	}

	public void pacmanBlockCollision() {
		pacman.changeDirection(blocks);
		pacman.blockCollision(blocks);
	}

	public void loadMap(int level) {
		blocks.clear();
		points.clear();
		ghosts.clear();
		pacman = null;
		switch (level) {
		case 1:
		case 2:
		case 3:
			currentBlock = block1;
			break;
		case 4:
		case 5:
		case 6:
			currentBlock = block2;
			break;
		case 7:
		case 8:
			currentBlock = block3;
			break;
		case 9:
		case 10:
			currentBlock = block4;
			break;
		}
		try {
			BufferedReader fo = new BufferedReader(new FileReader("maps/" + level + ".txt"));
			String a;
			char b;
			for (int i = 0; i < 16; i++) {// i is for axis x
				String mapLine = fo.readLine();
				for (int j = 0; j < 24; j++) {// j is for axis y
					a = mapLine.substring(j, j + 1);
					b = a.charAt(0);
					switch (b) {
					case '.':// point
						addPoint(j * 30, i * 30);
						break;
					case 'B':// block
						addBlock(j * 30, i * 30);
						break;
					case '-':// nothing
						break;
					case 'G':// ghost
						addGhost(j * 30, i * 30);
						addPoint(j * 30, i * 30);
						break;
					case 'P':// pacman
						pacman = new Pacman(j * 30, i * 30);
						break;
					}
				}
			}
			fo.close();
		} catch (FileNotFoundException e) {
			e.printStackTrace();
		} catch (IOException e) {
			e.printStackTrace();
		}

		for (int i = 0; i < ghosts.size(); i++) {
			Ghost gh = (Ghost) ghosts.get(i);
			gh.chooseStartingDirection(pacman.getCenterX(), pacman.getCenterY());
		}

		Rectangle pacmanRect = new Rectangle(0, 0, 0, 0);
		// finding direction where pacman can
		// only for start
		for (int i = 0; i < blocks.size(); i++) {
			Block block = (Block) blocks.get(i);
			// up
			pacmanRect.setRect(pacman.getCenterX(), pacman.getCenterY() - 30, 30, 30);
			if (pacmanRect.intersects(block.getRect())) {
				pacman.setCanUp(false);
			}
			// down
			pacmanRect.setRect(pacman.getCenterX(), pacman.getCenterY() + 30, 30, 30);
			if (pacmanRect.intersects(block.getRect())) {
				pacman.setCanDown(false);
			}
			// left
			pacmanRect.setRect(pacman.getCenterX() - 30, pacman.getCenterY(), 30, 30);
			if (pacmanRect.intersects(block.getRect())) {
				pacman.setCanLeft(false);
			}
			// right
			pacmanRect.setRect(pacman.getCenterX() + 30, pacman.getCenterY(), 30, 30);
			if (pacmanRect.intersects(block.getRect())) {
				pacman.setCanRight(false);
			}
		}
	}

	public void reloadLevel() {// when pacman is dead
		lives--;
		if (lives != 0) {
			pacman.setPacmanToStartingPosition();
			for (int i = 0; i < ghosts.size(); i++) {
				Ghost gh = (Ghost) ghosts.get(i);
				gh.setGhostToStartingPosition();
				gh.chooseStartingDirection(pacman.getCenterX(), pacman.getCenterY());
			}
		}
	}

	public void restartGame() {
		level = 0;
		score = 0;
		lives = startLives;
		pacmanIsDead = false;
		pause = false;
		gameOverPlayed = false;
		points.clear();// this break the level loop in run()
	}

	public void loadBestPlayer() throws IOException {
		BufferedReader fo = new BufferedReader(new FileReader("data/bestplayer.txt"));
		bestPlayer = fo.readLine();
		String a = fo.readLine();
		bestScore = Integer.parseInt(a);
		fo.close();
	}

	public void writeBestPlayer() throws IOException {
		PrintWriter out = new PrintWriter(new BufferedWriter(new FileWriter("data/bestplayer.txt")));
		out.println(bestPlayer);
		out.println(bestScore);
		out.close();
	}
