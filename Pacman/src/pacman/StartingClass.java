	@Override
	public void run() {
		while (true) {// infinity game loop
			do {
				if (!pacmanIsDead) {
					level++;
					level = (level == 11) ? 1 : level;
					// level 1-10; 11 is not valid
					loadMap(level);

					while (points.size() != 0 && lives > 0) {
						if (!pause) {
							pacman.update();
							for (int i = 0; i < ghosts.size(); i++) {
								Ghost gh = (Ghost) ghosts.get(i);
								gh.update(pacman.getCenterX(), // collision
										pacman.getCenterY());// with border

								gh.ghostCollision(ghosts, blocks, i, pacman.getCenterX(), pacman.getCenterY());
								// pacmans death
								if (pacman.getRect().intersects(gh.getRect())) {
									reloadLevel();
								}
							}

							animate();// set currentPacman and give shut pacman
										// when he stop();
							pacmanPointCollision();
							pacmanBlockCollision();
						} // endif pause
						repaint();

						try {
							Thread.sleep(17);// 1000ms/17ms=59fps
						} catch (InterruptedException e) {
							e.printStackTrace();
						}
					}
				}
			} while (lives > 0);// game loop while pacman is alive

			pacmanIsDead = true;// for paint gameOver img
			currentPacman = deadPacman;
			if (!gameOverPlayed) {// play once time
				gameOverSound.play();
				gameOverPlayed = true;
			}
			repaint();

			if (bestPlayer == null && score == bestScore) {
				bestPlayer = JOptionPane.showInputDialog("enter your nickname");
				if (bestPlayer != null) {// for cancel
					bestPlayer = (bestPlayer.length() < 11) ? bestPlayer : bestPlayer.substring(0, 10);
					bestPlayer = (bestPlayer.length() == 0) ? null : bestPlayer;
				} else {
					bestPlayer = null;
				}
				// bestPlayer max length 10 else do substring length 10
				if (bestPlayer != null) {// do until player write something
					try {
						writeBestPlayer();
					} catch (IOException e) {
						e.printStackTrace();
					}
				}
			}
		}
	}

	public void animate() {// set currentPacman and give shut pacman
							// when he stop();
		animRight.update(2);
		animLeft.update(2);
		animUp.update(2);
		animDown.update(2);

		switch (pacman.getDirection()) {
		case 1:
			currentPacman = animUp.getImage();
			break;
		case 2:
			currentPacman = animDown.getImage();
			break;
		case 3:
			currentPacman = animLeft.getImage();
			break;
		case 4:
			currentPacman = animRight.getImage();
			break;
		}

		if (pacman.getSpeedX() == 0 && pacman.getSpeedY() == 0) {
			switch (pacman.getDirection()) {
			case 1:
				currentPacman = animUp.getImage(0);
				break;
			case 2:
				currentPacman = animDown.getImage(0);
				break;
			case 3:
				currentPacman = animLeft.getImage(0);
				break;
			case 0:
			case 4:
				currentPacman = animRight.getImage(0);
				break;
			}
		}
	}

	@Override
	public void update(Graphics g) {
		if (image == null) {
			image = createImage(this.getWidth(), this.getHeight());
			second = image.getGraphics();
		}

		second.setColor(getBackground());
		second.fillRect(0, 0, getWidth(), getHeight());// everything is black,
														// repaint all
		paint(second);

		g.drawImage(image, 0, 0, this);
	}

	@Override
	public void paint(Graphics g) {
		g.setFont(new Font("Ubuntu", Font.BOLD, 14));
		g.setColor(Color.LIGHT_GRAY);
		g.fillRect(0, 480, 720, 30);

		g.setColor(Color.white);
		g.fillRect(25, 485, 140, 20);
		g.fillRect(170, 485, 135, 20);
		g.fillRect(310, 485, 125, 20);

		g.setColor(Color.black);
		g.drawString((bestPlayer == null) ? "Not set" : bestPlayer, 30, 500);
		g.drawString("Record: " + bestScore, 175, 500);
		g.drawString("Score: " + score, 315, 500);

		for (int i = 0; i < points.size(); i++) {
			Point p = (Point) points.get(i);
			g.drawImage(point, p.getX() + 7, p.getY() + 7, this);
		}

		for (int i = 0; i < blocks.size(); i++) {
			Block b = (Block) blocks.get(i);
			g.drawImage(currentBlock, b.getX(), b.getY(), this);
		}

		for (int i = 0; i < ghosts.size(); i++) {
			Ghost gh = (Ghost) ghosts.get(i);
			g.drawImage(ghost, gh.getCenterX(), gh.getCenterY(), this);
		}

		for (int i = 0; i < lives; i++) {
			g.drawImage(deadPacman2, 600 + (i * 30), 484, this);
		}

		g.drawImage(currentPacman, pacman.getCenterX(), pacman.getCenterY(), this);

		if (pacmanIsDead) {
			pause = false;
			g.drawImage(gameOver, 0, 0, this);
		}

		if (pause) {
			g.drawImage(paused, 0, 0, this);
		}
	}

	@Override
	public void keyPressed(KeyEvent e) {
		if (pacman.getDirection() == 0) {// for start game
			switch (e.getKeyCode()) {
			case KeyEvent.VK_UP:
				if (pacman.getCanUp()) {
					pacman.setDirection(1);
					pacman.moveUp();
				}
				break;
			case KeyEvent.VK_DOWN:
				if (pacman.getCanDown()) {
					pacman.setDirection(2);
					pacman.moveDown();
				}
				break;
			case KeyEvent.VK_LEFT:
				if (pacman.getCanLeft()) {
					pacman.setDirection(3);
					pacman.moveLeft();
				}
				break;
			case KeyEvent.VK_RIGHT:
				if (pacman.getCanRight()) {
					pacman.setDirection(4);
					pacman.moveRight();
				}
				break;
			case KeyEvent.VK_R:
				restartGame();
				break;
			case KeyEvent.VK_P:
				pause = (pause) ? false : true;
				break;
			// cheats
			case KeyEvent.VK_J:// go to next level
				points.clear();
				break;
			case KeyEvent.VK_L:
				lives = 4;
				break;
			}

			for (int i = 0; i < ghosts.size(); i++) {
				Ghost gh = (Ghost) ghosts.get(i);
				switch (gh.getDirection()) {
				case 1:
					gh.moveUp();
					break;
				case 2:
					gh.moveDown();
					break;
				case 3:
					gh.moveLeft();
					break;
				case 4:
					gh.moveRight();
					break;
				}
			}
		} else {
			switch (e.getKeyCode()) {
			case KeyEvent.VK_UP:
				pacman.setChangeDirection(1);
				break;
			case KeyEvent.VK_DOWN:
				pacman.setChangeDirection(2);
				break;
			case KeyEvent.VK_LEFT:
				pacman.setChangeDirection(3);
				break;
			case KeyEvent.VK_RIGHT:
				pacman.setChangeDirection(4);
				break;
			case KeyEvent.VK_R:
				restartGame();
				break;
			case KeyEvent.VK_P:
				pause = (pause) ? false : true;
				break;
			}
		}
	}

